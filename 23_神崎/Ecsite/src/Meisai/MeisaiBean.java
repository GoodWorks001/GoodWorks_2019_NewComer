package Meisai;

public class MeisaiBean {

	private int mei_id;
	private String date;
	private int mei_price;
	private int user_id;
	private int pro_cd;
	private int pro_rstock;

	public int getMei_id() {
		return mei_id;
	}
	public void setMei_id(int mei_id) {
		this.mei_id = mei_id;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getMei_price() {
		return mei_price;
	}
	public void setMei_price(int mei_price) {
		this.mei_price = mei_price;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getPro_cd() {
		return pro_cd;
	}
	public void setPro_cd(int pro_cd) {
		this.pro_cd = pro_cd;
	}
	public int getPro_rstock() {
		return pro_rstock;
	}
	public void setPro_rstock(int pro_rstock) {
		this.pro_rstock = pro_rstock;
	}



}
